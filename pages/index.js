import Head from "next/head";
import { useState, useEffect } from "react";
import {
  Chart as ChartJS,
  CategoryScale,
  LinearScale,
  PointElement,
  LineElement,
  BarElement,
  Title,
  Tooltip,
  Legend,
} from "chart.js";
import { Line, Bar } from "react-chartjs-2";
import { useAppContext } from "../components/context";
import {
  FormLayout,
  SliderInput,
  RadioButton,
  InputBox,
  SelectBox,
} from "../components/FormComponents";

ChartJS.register(
  CategoryScale,
  LinearScale,
  PointElement,
  LineElement,
  BarElement,
  Title,
  Tooltip,
  Legend
);

const TestPage = () => {
  const {
    Population,
    options,
    data,
    genMembers,
    targetString,
    setTargetString,
    populationSize,
    setPopulationSize,
    generationCount,
    setGenerationCount,
    correctValsCount,
    mutationInputRate,
    setMutationInputRate,
    isLoading,
    setIsLoading,
  } = useAppContext();

  const [count, setCount] = useState(targetString.length);
  const [chartType, setChartType] = useState("line");
  const [navIsOpen, setNavIsOpen] = useState(false);

  const handleGenerate = (
    e,
    populationSize,
    target,
    mutationRate,
    generations
  ) => {
    e.preventDefault();
    if (populationSize > 200 || !target.length || generations > 300) return;
    setIsLoading(true);
    const population = new Population(populationSize, target, mutationRate);
    population.evolve(generations);
    setIsLoading(false);
  };

  const handleGenerateOnMount = (
    populationSize,
    target,
    mutationRate,
    generations
  ) => {
    const population = new Population(populationSize, target, mutationRate);
    population.evolve(generations);
    setIsLoading(false);
  };

  const handleTextChange = (e) => {
    const target = e.target.value;
    const countValue = e.target.value.length;
    setTargetString(target.replace(/\s+/g, "").toLowerCase());
    setCount(countValue);
  };

  useEffect(() => {
    handleGenerateOnMount(
      populationSize,
      targetString,
      mutationInputRate,
      generationCount
    );
  }, []);
  return (
    <div className="min-h-screen lg:flex relative lg:pl-80">
      {/* overlay */}
      {navIsOpen && (
        <div
          onClick={() => setNavIsOpen(false)}
          className="fixed inset-0 w-full h-full bg-black opacity-50 lg:hidden"
        ></div>
      )}
      <Head>
        <title>Genetic Algorithm</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <button
        onClick={() => setNavIsOpen(true)}
        className="z-10 absolute top-3 left-3 p-2 focus:outline-none focus:bg-gray-600 hover:bg-gray-600 rounded-md lg:hidden"
      >
        <svg
          xmlns="http://www.w3.org/2000/svg"
          className="h-6 w-6"
          fill="none"
          viewBox="0 0 24 24"
          stroke="currentColor"
        >
          <path
            strokeLinecap="round"
            strokeLinejoin="round"
            strokeWidth="2"
            d="M4 6h16M4 12h16M4 18h16"
          />
        </svg>
      </button>

      <nav
        className={`${
          navIsOpen === true ? "translate-x-0" : "-translate-x-full"
        } fixed inset-0 lg:transform-none duration-200 z-10 w-60 sm:w-80 bg-slate-100 flex items-center justify-center`}
      >
        <button
          onClick={() => setNavIsOpen(false)}
          className="absolute top-5 right-5 focus:outline-none focus:bg-indigo-800 hover:bg-indigo-800 rounded-md lg:hidden"
        >
          <svg
            xmlns="http://www.w3.org/2000/svg"
            className="h-6 w-6"
            fill="none"
            viewBox="0 0 24 24"
            stroke="currentColor"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth="2"
              d="M15 19l-7-7 7-7"
            />
          </svg>
        </button>
        <div className="space-y-8 w-full px-4 sm:px-6">
          <h3 className="font-semibold">Parameters</h3>
          <form
            className="space-y-8"
            onSubmit={(e) =>
              handleGenerate(
                e,
                Math.abs(populationSize),
                targetString,
                mutationInputRate,
                Math.abs(generationCount)
              )
            }
          >
            <FormLayout inputLabel="Target String" condition="(letters only)">
              <div className="relative">
                <InputBox
                  type="text"
                  maxLength="4"
                  value={targetString}
                  onChange={handleTextChange}
                  isLoading={isLoading}
                />
                <p className="absolute top-4 right-2 font-light text-slate-400">
                  Limit: {count}/4
                </p>
              </div>
            </FormLayout>
            <FormLayout
              inputLabel="Population Size"
              condition="(min 20, max 200)"
            >
              <InputBox
                type="number"
                min="20"
                max="200"
                value={populationSize}
                onChange={(e) => setPopulationSize(e.target.value)}
                isLoading={isLoading}
              />
            </FormLayout>
            <FormLayout inputLabel="Mutation Rate">
              <SelectBox
                value={mutationInputRate}
                onChange={(e) => setMutationInputRate(e.target.value)}
                isLoading={isLoading}
              />
            </FormLayout>
            <FormLayout
              inputLabel="Generation Count"
              condition={`: ${generationCount}`}
            >
              <SliderInput
                value={generationCount}
                min={50}
                max={300}
                onChange={(e) => setGenerationCount(e.target.value)}
              />
            </FormLayout>
            <FormLayout inputLabel="Chart Type">
              <div
                onChange={(e) => setChartType(e.target.value)}
                className="space-x-8"
              >
                <RadioButton label="Line" value="line" chartType={chartType} />
                <RadioButton label="Bar" value="bar" chartType={chartType} />
              </div>
            </FormLayout>
            <button className="btn-primary" type="submit" disabled={isLoading}>
              Generate
            </button>
          </form>
        </div>
      </nav>
      {/* MAIN-CONTAINER */}
      <div className="main-container overflow-auto">
        <div className="space-y-4">
          <h1 className="font-bold">Genetic Algorithm</h1>
        </div>
        {/* table */}
        <div className="space-y-8">
          <h2 className="font-semibold">Evolutions Table</h2>
          <div className="overflow-auto h-96">
            <table className="">
              <thead>
                <tr>
                  <th className="text-center">Generation</th>
                  <th className="px-4 md:px-16 text-center">Fitness</th>
                  <th className="text-left">Population</th>
                </tr>
              </thead>
              <tbody className="divide-y">
                {genMembers.map((members, i) => (
                  <tr key={i}>
                    <td className="text-center p-4">{i + 1}</td>
                    <td className="text-center">{correctValsCount[i]}</td>
                    <td className="">{members.join("_")}</td>
                  </tr>
                ))}
              </tbody>
            </table>
          </div>
        </div>
        <div className="space-y-2">
          <h2 className="font-semibold">Fitness Value Chart</h2>
          <div>
            {chartType === "line" ? (
              <Line options={options} data={data} />
            ) : (
              <Bar options={options} data={data} />
            )}
          </div>
        </div>
      </div>
    </div>
  );
};

export default TestPage;
